int main(void)
{

  /* USER CODE BEGIN 1 */
	
	uint32_t flag=0;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  MX_USART3_UART_Init();
  MX_ADC1_Init();
  MX_I2C2_Init();
  MX_OPAMP1_Init();
  MX_SPI2_Init();
  MX_UART5_Init();
  MX_OPAMP2_Init();
  /* USER CODE BEGIN 2 */

	TMC5160_init_Config();
	USART1_Send_CMD_AutoCRC(0x00,0x2d,0x00,0x00,0xc8,0x00);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		
		HAL_UART_Receive(&huart5, Receive_data, 7, 1000);
		if (uart_status != HAL_OK) 
    {
        UART5_SendByte(0xE1); // 若通信错误，发送通信错误代码(E1)
        continue;             // 跳过本次循环，等待下一帧数据
    }
		
		switch(Receive_data[1])   												//To check the Receive_data[1]
		{	
			case 0xaa:																			//TMC5160-Motor initialize, AE AA
			
			TMC5160_CLEAR();
			flag=TMC5160_Initialize_Reset();
			
//			UART5_SendByte((flag == 0) ? 0xF0 : 0xE0);			//flag=0,UART5 send F0. Otherwise,E0 is send.
			flag=0;
						
			// 清理接收的指令数据，确保下次接收正常
       memset(Receive_data, 0, sizeof(Receive_data)); // 清空指令缓冲区
       UART_BUF = 0;                                 // 清空全局UART缓冲
       break;
						
			
			
			case 0xab:																			// TMC5160-Motor Positve step move
			
			HAL_Delay(5);
			TMC5160_init_Config();
			TMC5160_CLEAR();


			uint8_t data1=Receive_data[2];
			uint8_t	data2=Receive_data[3];
			uint8_t	data3=Receive_data[4];
			uint8_t	data4=Receive_data[5];
	
			USART1_Send_CMD_AutoCRC(0x00,0x2d,data1,data2,data3,data4);
//			TMC5160_CLEAR();
					  break;


		default: 
						break;
		}	
		
		
  }
  /* USER CODE END 3 */
}

